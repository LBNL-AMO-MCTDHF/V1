module mycdotmod
contains
  recursive function mycdot(one,two,n)
    implicit none
    integer,intent(in) :: n
    DATATYPE,intent(in) :: one(n), two(n)
    DATATYPE :: mycdot, sum
    integer :: i
    sum=0.d0
    do i=1,n
       sum = sum + one(i) * two(i) 
    enddo
    mycdot=sum
  end function mycdot
  recursive function mydot(one,two,n)
    implicit none
    integer,intent(in) :: n
    DATATYPE,intent(in) :: one(n), two(n)
    DATATYPE :: mydot, sum
    integer :: i
    sum=0.d0
    do i=1,n
       sum = sum + CONJUGATE(one(i)) * two(i) 
    enddo
    mydot=sum
  end function mydot
end module mycdotmod


