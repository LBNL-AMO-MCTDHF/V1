        out(:,(i-1)*myblocksize+1:i*myblocksize,:,ii)=outtemp(:,:,:,ii,i)
     enddo
  enddo
!$OMP END DO
!   why?  why not
!$OMP BARRIER  
!$OMP END PARALLEL

  call myclock(btime); times(3)=times(3)+btime-atime;

end subroutine mytranspose

