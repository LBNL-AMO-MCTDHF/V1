!!$TINV    enddo
!!$TINV  
!!$TINV    call mpibarrier()   !!TEMP?
!!$TINV  
!!$TINV    if (debugflag.ne.0) then
!!$TINV       OFLWR "op_tinv_scaled: iterations=",numcalled; CFL
