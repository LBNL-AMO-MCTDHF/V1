  else
     mycoefs(:) = (/ 1d0, 1d0, 1d0 /)
  endif

  do jj=1,3

  if (abs(mycoefs(jj)).gt.0d0) then

     if (scalingflag.ne.0) then
        if (option.eq.1) then        !! KE
           do ii=1,howmany
              temp(:,ii)=in(:,ii)*invjacobian(:,jj)
           enddo
        else                         !! FIRST DER
           do ii=1,howmany
              temp(:,ii)=in(:,ii)*invsqrtjacobian(:,jj)
           enddo
        endif
     else if (maskflag.ne.0) then
