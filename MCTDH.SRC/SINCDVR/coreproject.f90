  do jj=1,mmm
     call ZGEMM('N','T',nnn,outdim,indim,(1d0,0d0),in(:,:,jj),nnn,mat,outdim,(0d0,0d0), out(:,:,jj), nnn)
  enddo

end subroutine mult_all0_big_gen_complex


