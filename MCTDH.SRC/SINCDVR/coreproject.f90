  use myparams
  use pmpimod
  use pfileptrmod
  use myprojectmod  
  implicit none
  integer,intent(in) :: nnn,option,howmany,indim,notiming
  DATATYPE,intent(in) :: in(nnn*numpoints(indim),howmany)
